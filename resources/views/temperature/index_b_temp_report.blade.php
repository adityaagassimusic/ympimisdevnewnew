					tableData += '<td>'+ value.company +'</td>';
					tableData += '<td>'+ value.kota +'</td>';
					tableData += '<td>'+ value.name +'</td>';
					tableData += '<td>'+ value.suhu +'</td>';
					tableData += '</tr>';
				});
				$('#example1Body').append(tableData);

				var table = $('#example1').DataTable({
					'dom': 'Bfrtip',
					'responsive':true,
					'lengthMenu': [
					[ 10, 25, 50, -1 ],
					[ '10 rows', '25 rows', '50 rows', 'Show all' ]
					],
					'buttons': {
						buttons:[
						{
